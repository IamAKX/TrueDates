package com.neosao.truedates.model;

public class MotherToungeModel {
    private String mothertoungeName;
    private String code;

    public MotherToungeModel(String mothertoungeName, String code) {
        this.mothertoungeName = mothertoungeName;
        this.code = code;
    }

    public String getMothertoungeName() {
        return mothertoungeName;
    }

    public String getCode() {
        return code;
    }
}
